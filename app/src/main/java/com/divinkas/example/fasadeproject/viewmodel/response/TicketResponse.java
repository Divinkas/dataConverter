package com.divinkas.example.fasadeproject.viewmodel.response;

public class TicketResponse extends BaseResponse {
    private int idTicket;
    private String nameTicket;
    private String dateTicket;

    public TicketResponse() {
    }

    public int getIdTicket() {
        return idTicket;
    }

    public void setIdTicket(int idTicket) {
        this.idTicket = idTicket;
    }

    public String getNameTicket() {
        return nameTicket;
    }

    public void setNameTicket(String nameTicket) {
        this.nameTicket = nameTicket;
    }

    public String getDateTicket() {
        return dateTicket;
    }

    public void setDateTicket(String dateTicket) {
        this.dateTicket = dateTicket;
    }
}
